"""
Comparision Operators are the operators used to compared the values of operands .
Returns (BOOLEAN) value

>     --> Greater Than 
<     --> Less Than
>=    --> Greater Than Equal to
<=    --> Less Than Equal to
==    --> Equals to (Checking for similar values)
!=    --> Not Equals

"""

a = 7
b = 15
print(a > b)  # --> False
print(b > a)  # --> True
print(a >= b)  # --> False
print(a <= b)  # --> True
print(a == b)  # --> False
print(a != b)  # --> True
