# Arithmetic Operators -
"""Arithmetic Operators are used to do mathematical or arithmetic operations
on two or more operands.
"""
a = 10
b = 3
print(a + b)  # Addition --> 2 + 3 = 5
print(a - b)  # Subtraction --> 9 - 5 = 4
print(a * b)  # Multiplication ---> 3 * 4 = 12
print(a / b)  # Division (Quotient) ---> 15 / 5 = 3.0 -> returns float
print(a // b)  # Floor Division ---> 19 // 2 = 9 -> return floor value
print(a % b)  # Remained (Division) -->9 % 2 = 1 | -19 % 4 = 1->
# Works a bit different for negative dividend
print(a**b)  # Power/ Exponent --> 2 ** 3 = 8
